// CARGAR CON FILTROS
    private void cargarSalidas(String filtroOrden, String filtroProducto) {
        String sql = "SELECT s.id, s.fecha_salida, p.nombre AS producto, s.cantidad_salida, " +
                     "s.numero_orden, s.destino, s.observaciones, u.usuario " +
                     "FROM salida s " +
                     "JOIN producto p ON s.id_producto = p.id " +
                     "JOIN usuarios u ON s.id_usuario = u.id " +
                     "WHERE 1=1 " +
                     filtroOrden +
                     filtroProducto +
                     " ORDER BY s.fecha_salida DESC";

        String[] titulos = {"ID", "Fecha", "Producto", "Cantidad", "N° Orden", "Destino", "Observaciones", "Usuario"};
        DefaultTableModel modelo = new DefaultTableModel(null, titulos) {
            @Override
            public boolean isCellEditable(int row, int column) { return false; }
        };
        tblSalidas.setModel(modelo);

        try (Connection cn = Conexion.getConexion();
             PreparedStatement pst = cn.prepareStatement(sql);
             ResultSet rs = pst.executeQuery()) {

            while (rs.next()) {
                Object[] fila = {
                    rs.getInt("id"),
                    new SimpleDateFormat("dd/MM/yyyy HH:mm").format(rs.getTimestamp("fecha_salida")),
                    rs.getString("producto"),
                    rs.getDouble("cantidad_salida"),
                    rs.getString("numero_orden"),
                    rs.getString("destino"),
                    rs.getString("observaciones"),
                    rs.getString("usuario")
                };
                modelo.addRow(fila);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar salidas: " + e.getMessage());
            e.printStackTrace();
        }
    }
    
    // LLENAR COMBO DE PRODUCTOS PARA FILTRO
    private void cargarProductosFiltro() {
        cbxFiltroProducto.addItem(new ITProducto(0, "Todos los productos"));
        try (Connection cn = Conexion.getConexion();
             PreparedStatement pst = cn.prepareStatement("SELECT id, nombre FROM producto ORDER BY nombre")) {
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                cbxFiltroProducto.addItem(new ITProducto(rs.getInt("id"), rs.getString("nombre")));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    

// MÉTODO PARA EXPORTAR LA TABLA ACTUAL A CSV
private void exportarTablaActualACSV() {
    if (tblSalidas.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "No hay datos para exportar", 
                                    "Advertencia", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Selector de archivo
    JFileChooser selector = new JFileChooser();
    selector.setDialogTitle("Guardar lista de salidas como CSV");

    String fechaHora = new SimpleDateFormat("dd-MM-yyyy_HHmm").format(new java.util.Date());
    String nombreSugerido = "Lista_Salidas_" + fechaHora + ".csv";
    selector.setSelectedFile(new File(nombreSugerido));

    FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivo CSV (*.csv)", "csv");
    selector.setFileFilter(filtro);

    int opcion = selector.showSaveDialog(this);
    if (opcion != JFileChooser.APPROVE_OPTION) {
        return; // Canceló
    }

    File archivo = selector.getSelectedFile();
    if (!archivo.getName().toLowerCase().endsWith(".csv")) {
        archivo = new File(archivo.getAbsolutePath() + ".csv");
    }

    // Confirmar sobrescritura
    if (archivo.exists()) {
        int confirmar = JOptionPane.showConfirmDialog(this,
            "El archivo ya existe. ¿Reemplazar?", "Confirmar", JOptionPane.YES_NO_OPTION);
        if (confirmar != JOptionPane.YES_OPTION) {
            return;
        }
    }

    // EXPORTAR DATOS DE LA TABLA
    try (PrintWriter pw = new PrintWriter(archivo, "UTF-8")) {
        // ENCABEZADOS (sin la columna ID oculta)
        for (int col = 1; col < tblSalidas.getColumnCount(); col++) {
            pw.print(tblSalidas.getColumnName(col));
            if (col < tblSalidas.getColumnCount() - 1) pw.print(",");
        }
        pw.println();

        // FILAS VISIBLES
        for (int row = 0; row < tblSalidas.getRowCount(); row++) {
            for (int col = 1; col < tblSalidas.getColumnCount(); col++) { // Salta ID
                Object valor = tblSalidas.getValueAt(row, col);
                String texto = (valor != null) ? valor.toString() : "";
                // Escapar comas y comillas para CSV correcto
                if (texto.contains(",") || texto.contains("\"") || texto.contains("\n")) {
                    texto = "\"" + texto.replace("\"", "\"\"") + "\"";
                }
                pw.print(texto);
                if (col < tblSalidas.getColumnCount() - 1) pw.print(",");
            }
            pw.println();
        }

        JOptionPane.showMessageDialog(this,
            "¡Lista de salidas exportada correctamente!\n" + archivo.getAbsolutePath(),
            "Éxito", JOptionPane.INFORMATION_MESSAGE);

        // Abrir automáticamente
        Desktop.getDesktop().open(archivo);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this,
            "Error al exportar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
}
    
    

