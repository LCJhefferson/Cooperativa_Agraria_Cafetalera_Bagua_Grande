    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
int fila = tblSalidas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una salida para eliminar");
            return;
        }
        if (JOptionPane.showConfirmDialog(this, "¿Eliminar esta salida permanentemente?", "Confirmar",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            
            int id = (int) tblSalidas.getValueAt(fila, 0);
            try (Connection cn = Conexion.getConexion();
                 PreparedStatement pst = cn.prepareStatement("DELETE FROM salida WHERE id = ?")) {
                pst.setInt(1, id);
                pst.executeUpdate();
                cargarTodasLasSalidas();
                JOptionPane.showMessageDialog(this, "Salida eliminada");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    
        // TODO add your handling code here:
    }//GEN-LAST:event_btnEliminarActionPerformed

