public void actualizarNavegacion(String titulo, String... ruta) {
    LBLTitulo.setText(titulo);

    StringBuilder ubicacion = new StringBuilder("Menú");
    for (String paso : ruta) {
        ubicacion.append("/").append(paso);
    }
    LBLUbicacion.setText(ubicacion.toString());
}

